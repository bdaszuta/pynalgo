# Tridiagonal
###############################################################################

def solver_tridiagonal(a : NDArray[Any],
                       b : NDArray[Any],
                       c : NDArray[Any],
                       y : NDArray[Any]) -> NDArray[Any]:
  '''
  Solver for x in Tx=y where T is tridiagonal with structure:

    b_0     c_0      0      ...
    a_0     b_1     c_1      0      ...
     0      a_1     b_2     c_2      0      ...
     .       0       .       .       .       .
             .       0    a_{n-3} b_{n-2} c_{n-2}
                     .       0    a_{n-2} b_{n-1}
  Therefore for P = [nxn]:
    a = [n-1]
    b = [n]
    c = [n-1]

  This uses the Thomas algorithm.

  Inputs are preserved.

  Note
  ----
  No checks are performed as to whether P is invertible.
  '''
  sz = b.size

  # create scratch arrays for Thomas algorithm
  c_star = empty(sz-1, dtype=y.dtype)
  d_star = empty(sz, dtype=y.dtype)
  x = empty(sz, dtype=y.dtype)

  # Forward sweep
  c_star[0] = c[0] / b[0]
  d_star[0] = y[0] / b[0]

  for ix in range(1, sz-1):
    m = 1. / (b[ix] - a[ix-1] * c_star[ix-1])
    c_star[ix] = c[ix] * m
    d_star[ix] = (y[ix] - a[ix-1] * d_star[ix-1]) * m

  m = 1. / (b[sz-1] - a[sz-2] * c_star[sz-2])
  d_star[sz-1] = (y[sz-1] - a[sz-2] * d_star[sz-2]) * m

  # Backward sweep
  x[sz-1] = d_star[sz-1]
  for ix in range(sz-2, -1, -1):
    x[ix] = d_star[ix] - c_star[ix] * x[ix+1]
  return x

